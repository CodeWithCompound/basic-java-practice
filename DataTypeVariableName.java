public class DataTypeVariableName {
    public static void main(String[] args) {
        // Different data types
        int integerVar = 10;               // Integer
        double doubleVar = 20.5;           // Double
        char charVar = 'A';                // Character
        String stringVar = "Hello";        // String
        boolean booleanVar = true;         // Boolean

        // Print variables
        System.out.println("Integer: " + integerVar);
        System.out.println("Double: " + doubleVar);
        System.out.println("Character: " + charVar);
        System.out.println("String: " + stringVar);
        System.out.println("Boolean: " + booleanVar);

        // Primitive data types
        byte byteVar = 100;                // Byte
        short shortVar = 10000;            // Short
        long longVar = 100000L;           // Long
        float floatVar = 10.5f;           // Float

        // Print primitive variables
        System.out.println("Byte: " + byteVar);
        System.out.println("Short: " + shortVar);
        System.out.println("Long: " + longVar);
        System.out.println("Float: " + floatVar);
        
    }
}
